package groupFiles;

public class Costume {
	public String name;
	public String color; //attributes 
	public String subject; 
	public String politicalParty; 
	public String genre; 
	public String sportCategory; 
	
	public Costume(String name, String color, String subject, String politicalParty, 
			String genre, String sportCategory){
		
		this.name = name;
		this.color = color; 
		this.subject = subject; 
		this.politicalParty = politicalParty; 
		this.genre = genre;
		this.sportCategory = sportCategory; 
		
	}
}

